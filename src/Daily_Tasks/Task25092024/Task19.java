package Daily_Tasks.Task25092024;
//Table of number n = 10, print table with For or while.
public class Task19 {
    public static void main(String[] args) {
        int n=10;

        // For loop to generate the table of 10
        for (int i = 1; i <= 10; i++) {
            System.out.println(n + " x " + i + " = " + (n * i));
        }
    }
}
